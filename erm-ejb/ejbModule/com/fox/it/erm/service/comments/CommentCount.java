package com.fox.it.erm.service.comments;

public class CommentCount {

	private Long strandComments;
	private Long strandRestrictionComments;
	private Long productRestrictionComments;
	
	public CommentCount() {
	}

	public Long getStrandComments() {
		return strandComments;
	}

	public void setStrandComments(Long strandComments) {
		this.strandComments = strandComments;
	}

	public Long getStrandRestrictionComments() {
		return strandRestrictionComments;
	}

	public void setStrandRestrictionComments(Long strandRestrictionComments) {
		this.strandRestrictionComments = strandRestrictionComments;
	}

	public Long getProductRestrictionComments() {
		return productRestrictionComments;
	}

	public void setProductRestrictionComments(Long productRestrictionComments) {
		this.productRestrictionComments = productRestrictionComments;
	}
	
	

}
